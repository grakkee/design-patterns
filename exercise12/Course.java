public class Course {
   private String description;
   private String name;
   private int number;
   
   public String getDescription() {
      return description;
   }
   
   public void setDescription(String d) {
      this.description = d;
   }
   
   public String getName() {
      return name;
   }
   
   public void setName(String name) {
      this.name = name;
   }

   public void setNumber(int num) {
      this.number = num;
   }

   public int getNumber() {
      return number;
   }
}
  